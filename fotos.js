const gallery = document.getElementById('galeria');

galleryData.forEach(item => {
    const div = document.createElement('div');
    div.className = 'galeria-item';


    const picture = document.createElement('picture');
    item.sources.forEach(sourceData => {
        const source = document.createElement('source');
        source.media = sourceData.media;
        source.srcset = sourceData.srcset;
        picture.appendChild(source);
    });

    const img = document.createElement('img');
    img.src = item.src;
    img.alt = item.alt;

    picture.appendChild(img);
    div.appendChild(picture);

    // Descripcion de la foto
    const descripcion = document.createElement('div');
    descripcion.className = 'descripcion';
    descripcion.innerText = item.alt;
    div.appendChild(descripcion);

    gallery.appendChild(div);

    // Ventana modal
    div.addEventListener('click', () => {
        document.getElementById('modal').style.display = "block";
        document.getElementById('modal-img').src = item.src;
        document.getElementById('modal-descripcion').innerText = item.alt;
    });
});
//Cerrar ventana modal
document.querySelector('.close').onclick = function () {
    document.getElementById('modal').style.display = "none";
}